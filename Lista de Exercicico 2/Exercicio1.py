# Exercício 1: Soma simples

# Passos:1. Pedir ao usuário dois números inteiros. 2. Somar os dois valores. 3. Mostrar o resultado da soma.

print('Soma Simples')
numero1 = float(input('Digite um numero para a soma: '))
numero2 = float(input('Digite mais um numero para a soma:'))
resultado = numero1+numero2
print(f'O resultado de {numero1} + {numero2} é {resultado}')
