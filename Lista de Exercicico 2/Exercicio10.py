# Exercício 10: Promoção infantil

# Passos: 
# 1. Pedir a idade da criança. 
# 2. Verificar se a idade é menor ou igual a 12. 
# 3. Mostrar mensagem informando se o ingresso é gratuito ou não.

print('Promoção infnatil')
idade= int(input('Digite a idade da criança: '))

