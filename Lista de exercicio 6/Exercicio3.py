# · Exercício 3 – Mostrar tabuada do 5

# · • Crie uma função que não recebe parâmetros.

# · • Use um laço for para repetir os números de 1 a 10.

# · • Em cada repetição, calcule o produto de 5 pelo número atual e mostre o resultado.

# · • A função só imprime os resultados, não retorna nada.

def tabuada_do_5():
    for i in range(1, 11):
        print(f"5 x {i} = {5 * i}")

tabuada_do_5()


